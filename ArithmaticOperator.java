package JavaOperator;

public class ArithmaticOperator {

	public static void main(String[] args) {
		int a=20;
		int b=10;
		int result = a+b;
		
		
		System.out.println("a+b="+result);
		result =a-b;
		
		System.out.println("a-b="+result);
		result =a*b;
		
		System.out.println("a*b="+result);
		result =b/a;
		
		System.out.println("b/a="+result);
		result =a%3;
        
		System.out.println("a%3="+result);
	}

}

package DecisionMakingStatements;

public class Switch_Statement {

	public static void main(String[] args) {
		
		int num =3;
		
		switch(num) {
		
		case 0:
			System.out.println("number is 0 :Coforge");
		    break;
		    
		case 1:
			System.out.println("number is 1 :Pallab");
		    break;
		    
		case 2:
			System.out.println("number is 2 :Chaithu");
		    break;
		    
		case 3:
			System.out.println("number is 3 :Bengaluru");
		    break;
		    default :
		    	System.out.println(num);
		}
	}

}